/**
 * reverse_array - | starts the file
 * @a: - | var
 * @n: - | var
 * Return: - | ends the file
 */

void reverse_array(int *a, int n)
{

int i;
int y = n - 1;
int x;
int co = 0;

for (i = 0; i < y; i++)
{
x[i] = a[y];
y--;
}

while (co < (n-1))
{
a[co] = x[co];
co++;
}

}
